package Dz;

public class NumberPi {

	public static void main(String[] args) {
		double pi = Math.PI;
		System.out.printf("Число Пи: %.1f \n", pi);
		System.out.printf("Число Пи: %.2f \n", pi);
		System.out.printf("Число Пи: %.3f \n", pi);
		System.out.printf("Число Пи: %.4f \n", pi);
		System.out.printf("Число Пи: %.5f \n", pi);

	}

}
